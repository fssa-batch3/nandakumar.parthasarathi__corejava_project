package day02misc;

public class Academy {
	int id;
	String code;
	String name;

	public String getName() {
		return name;
	}

	public void setName(String newName) {
		newName = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int newId) {
		newId = id;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String newCode) {
		newCode = code;
	}

}